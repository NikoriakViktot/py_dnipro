import matplotlib.pyplot as plt
import networkx as nx

# Створимо графік залежностей
G = nx.DiGraph()

# Додаємо вузли
nodes = {
    "Увага": {},
    "Префронтальна кора (PFC)": {"Функції": "Плани, прийняття рішень, когнітивний контроль та регуляція поведінки",
                                  "Роль в увазі": "Підтримка концентрації, розподіл уваги, регуляція емоцій"},
    "Поясна звивина (ACC)": {"Функції": "Контроль конфліктів, моніторинг помилок, регуляція емоцій",
                             "Роль в увазі": "Моніторинг конфліктів і помилок, підтримка уваги, переключення між завданнями"},
    "Париетальна кора": {"Функції": "Просторове орієнтування, інтеграція сенсорної інформації",
                         "Роль в увазі": "Просторова увага, вибіркова увага"},
    "Задня частина поясної звивини (PCC)": {"Функції": "Саморефлексія, обробка інформації, пов'язаної з собою",
                                            "Роль в увазі": "Підтримка внутрішньої уваги"},
    "Лімбічна система": {"Функції": "Регуляція емоцій, пам'ять",
                         "Роль в увазі": "Регуляція емоційних реакцій та пам'яті"},
    "Ретикулярна формація": {"Функції": "Контроль збудження та уваги",
                             "Роль в увазі": "Регуляція рівня збудження і стану бадьорості"},
    "Таламус": {"Функції": "Обробка сенсорної інформації, передача сигналів до кори головного мозку",
                "Роль в увазі": "Фільтрація сенсорної інформації, вибіркова увага"}
}

# Додаємо вузли до графу
for node, attributes in nodes.items():
    G.add_node(node, **attributes)

# Додаємо ребра
edges = [
    ("Увага", "Префронтальна кора (PFC)"),
    ("Увага", "Поясна звивина (ACC)"),
    ("Увага", "Париетальна кора"),
    ("Увага", "Задня частина поясної звивини (PCC)"),
    ("Увага", "Лімбічна система"),
    ("Увага", "Ретикулярна формація"),
    ("Увага", "Таламус")
]

G.add_edges_from(edges)

# Створюємо макет
pos = nx.spring_layout(G, seed=42)

# Візуалізація графу
plt.figure(figsize=(16, 12))
nx.draw(G, pos, with_labels=True, node_color='skyblue', node_size=3500, font_size=9, font_weight='bold', arrowsize=20, edge_color='gray')

# Додаємо атрибути до вузлів
node_labels = {node: f"{node}" for node, attrs in nodes.items()}
nx.draw_networkx_labels(G, pos, labels=node_labels, font_size=8)

# Додаємо підписи до ребер
edge_labels = {(u, v): f"Функції: {nodes[v]['Функції']}\nРоль: {nodes[v]['Роль в увазі']}" for u, v in edges}
nx.draw_networkx_edge_labels(G, pos, edge_labels=edge_labels, font_size=8, label_pos=0.5, bbox=dict(facecolor='white', edgecolor='none', boxstyle='round,pad=0.5'))

plt.title("Графік залежностей: Увага та задіяні частини мозку", fontsize=15)
plt.show()


